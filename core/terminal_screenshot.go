// glyphBitmap returns the 9-row bitmap for a printable ASCII rune.
func glyphBitmap(r rune) [9]uint8 {
	if bm, ok := glyphBitmaps[r]; ok {
		return bm
	}
	return [9]uint8{0x1F, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1F, 0x00, 0x00}
}
