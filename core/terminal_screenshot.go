// termFont describes a fixed-width bitmap font for terminal rendering.
// Each cell is cellW×cellH pixels.
const (
	cellW = 8
	cellH = 16
)

// ansiColor maps the standard 8 ANSI foreground colors (30-37) to RGB.
var ansiColors = [8]color.RGBA{
	{0, 0, 0, 255},       // black
	{187, 0, 0, 255},     // red
	{0, 187, 0, 255},     // green
	{187, 187, 0, 255},   // yellow
	{0, 0, 187, 255},     // blue
	{187, 0, 187, 255},   // magenta
	{0, 187, 187, 255},   // cyan
	{187, 187, 187, 255}, // white
}

var ansiColorsBright = [8]color.RGBA{
	{85, 85, 85, 255},    // bright black (dark grey)
	{255, 85, 85, 255},   // bright red
	{85, 255, 85, 255},   // bright green
	{255, 255, 85, 255},  // bright yellow
	{85, 85, 255, 255},   // bright blue
	{255, 85, 255, 255},  // bright magenta
	{85, 255, 255, 255},  // bright cyan
	{255, 255, 255, 255}, // bright white
}

var (
	defaultFG = color.RGBA{187, 187, 187, 255}
	defaultBG = color.RGBA{30, 30, 30, 255}
)

type cell struct {
	ch rune
	fg color.RGBA
	bg color.RGBA
}

// RenderANSIToPNG converts an ANSI-escaped terminal buffer to a PNG image.
// Returns the PNG bytes.
func RenderANSIToPNG(ansiText string) ([]byte, error) {
	grid := parseANSI(ansiText)
	if len(grid) == 0 {
		return nil, nil
	}

	cols := 0
	for _, row := range grid {
		if len(row) > cols {
			cols = len(row)
		}
	}
	rows := len(grid)

	img := image.NewRGBA(image.Rect(0, 0, cols*cellW, rows*cellH))

	// Fill background
	for y := range img.Bounds().Dy() {
		for x := range img.Bounds().Dx() {
			img.SetRGBA(x, y, defaultBG)
		}
	}

	for r, row := range grid {
		for c, cl := range row {
			drawCell(img, c*cellW, r*cellH, cl)
		}
	}

	var buf bytes.Buffer
	if err := png.Encode(&buf, img); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

// parseANSI strips ANSI escape sequences and builds a 2-D cell grid.
func parseANSI(s string) [][]cell {
	var rows [][]cell
	var curRow []cell

	fg := defaultFG
	bg := defaultBG
	bright := false

	i := 0
	for i < len(s) {
		if s[i] == '\n' {
			rows = append(rows, curRow)
			curRow = nil
			i++
			continue
		}
		if s[i] == '\r' {
			i++
			continue
		}
		// ESC sequence
		if s[i] == '\x1b' && i+1 < len(s) && s[i+1] == '[' {
			end := strings.IndexByte(s[i+2:], 'm')
			if end < 0 {
				// skip to next printable
				i++
				continue
			}
			seq := s[i+2 : i+2+end]
			i = i + 2 + end + 1
			parseCSIm(seq, &fg, &bg, &bright)
			continue
		}
		// skip other ESC sequences
		if s[i] == '\x1b' {
			i++
			// skip until letter
			for i < len(s) && (s[i] < 'A' || s[i] > 'z') {
				i++
			}
			if i < len(s) {
				i++
			}
			continue
		}
		r, size := utf8.DecodeRuneInString(s[i:])
		i += size
		if r == utf8.RuneError {
			continue
		}
		curRow = append(curRow, cell{ch: r, fg: fg, bg: bg})
	}
	if len(curRow) > 0 {
		rows = append(rows, curRow)
	}
	return rows
}

// parseCSIm parses SGR (Select Graphic Rendition) parameters.
func parseCSIm(seq string, fg, bg *color.RGBA, bright *bool) {
	if seq == "" || seq == "0" {
		*fg = defaultFG
		*bg = defaultBG
		*bright = false
		return
	}
	parts := strings.Split(seq, ";")
	for idx := 0; idx < len(parts); idx++ {
		p := parts[idx]
		switch p {
		case "0", "":
			*fg = defaultFG
			*bg = defaultBG
			*bright = false
		case "1":
			*bright = true
		case "22":
			*bright = false
		default:
			n := 0
			for _, ch := range p {
				if ch >= '0' && ch <= '9' {
					n = n*10 + int(ch-'0')
				}
			}
			switch {
			case n >= 30 && n <= 37:
				if *bright {
					*fg = ansiColorsBright[n-30]
				} else {
					*fg = ansiColors[n-30]
				}
			case n >= 90 && n <= 97:
				*fg = ansiColorsBright[n-90]
			case n >= 40 && n <= 47:
				*bg = ansiColors[n-40]
			case n >= 100 && n <= 107:
				*bg = ansiColorsBright[n-100]
			case n == 39:
				*fg = defaultFG
			case n == 49:
				*bg = defaultBG
			case n == 38 || n == 48:
				// 256-color or truecolor — skip extra params
				if idx+1 < len(parts) && parts[idx+1] == "5" && idx+2 < len(parts) {
					idx += 2
				} else if idx+1 < len(parts) && parts[idx+1] == "2" && idx+4 < len(parts) {
					idx += 4
				}
			}
		}
	}
}

// drawCell renders one character cell at pixel (px, py) into img using a
// simple 5×9 embedded bitmap for printable ASCII (space + 0x21-0x7e).
func drawCell(img *image.RGBA, px, py int, cl cell) {
	// Fill cell background
	for dy := 0; dy < cellH; dy++ {
		for dx := 0; dx < cellW; dx++ {
			img.SetRGBA(px+dx, py+dy, cl.bg)
		}
	}
	if cl.ch <= ' ' || cl.ch > '~' {
		return
	}
	// Draw foreground pixels using glyph bitmap
	bm := glyphBitmap(cl.ch)
	for row := 0; row < 9; row++ {
		bits := bm[row]
		for col := 0; col < 5; col++ {
			if bits&(1<<uint(4-col)) != 0 {
				x := px + 1 + col
				y := py + 3 + row
				if x < px+cellW && y < py+cellH {
					img.SetRGBA(x, y, cl.fg)
				}
			}
		}
	}
}

